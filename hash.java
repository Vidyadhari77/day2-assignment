import java.util.*;
import java.lang.*;
public class hashvalue {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        String s1= sc.next();
        String s2= sc.next();
        System.out.println("string s1 hashcode"+s1.hashCode());
        System.out.println("String s2 hashcode"+s2.hashCode());
    }
    
}
